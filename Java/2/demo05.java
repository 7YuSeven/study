public class demo05{
	public static void main(String[] args){
		//杅擂濬倰妏蚚
		byte b = 10;
		System.out.println(b);
		
		int i = 20;
		System.out.println(i);
		
		short s = 10;
		System.out.println(s);
		
		long l = 500L;
		System.out.println(l);
		
		float f = 10.23F;
		System.out.println(f);
		
		double d = 25.21;
		System.out.println(d);
		
		char c = 'a';
		System.out.println(c);
		
		boolean bool = true;
		System.out.println(bool);
	}
}